// ─── Directional (8-way) character facing ────────────────────────────────────
// Player and basic enemies are PNG-backed directional sprites instead of a
// single grid rotated to the aim/movement vector. Screen space is +x east/right
// and +y south/down, so Phaser's atan2(dy, dx) increases clockwise from east.
export const DIRECTIONS_8 = ['right', 'down_right', 'down', 'down_left', 'left', 'up_left', 'up', 'up_right'];

export function directionFromAngle(angle) {
  const step = Math.PI / 4;
  const k = ((Math.round(angle / step) % 8) + 8) % 8;
  return DIRECTIONS_8[k];
}

// Only five base facings are authored per character; the three left-side facings
// are the right-side textures mirrored horizontally (flashlight switches hands,
// invisible from overhead).
const MIRROR_FRAME = {
  right:      { base: 'right',      flipX: false },
  down_right: { base: 'down_right', flipX: false },
  down:       { base: 'down',       flipX: false },
  up:         { base: 'up',         flipX: false },
  up_right:   { base: 'up_right',   flipX: false },
  left:       { base: 'right',      flipX: true },
  down_left:  { base: 'down_right', flipX: true },
  up_left:    { base: 'up_right',   flipX: true },
};

export function getDirectionalFrame(prefix, angle) {
  const { base, flipX } = MIRROR_FRAME[directionFromAngle(angle)];
  return { textureKey: `${prefix}_${base}`, flipX, base };
}

export function getAnimKeyForState(entityType, aiState) {
  const prefix = entityType === 'basic' ? 'enemy' : entityType;
  if (aiState === 'attack' && entityType === 'spitter') return 'spitter_attack';
  if (aiState === 'chase' || aiState === 'search') return `${prefix}_walk`;
  return `${prefix}_idle`;
}
