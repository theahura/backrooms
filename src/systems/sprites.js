export function getEnemyTextureKey(type) {
  if (type === 'crawler') return 'crawler_idle_0';
  if (type === 'spitter') return 'spitter_idle_0';
  return 'enemy_idle_0';
}

export function getAnimKeyForState(entityType, aiState) {
  const prefix = entityType === 'basic' ? 'enemy' : entityType;
  if (aiState === 'attack' && entityType === 'spitter') return 'spitter_attack';
  if (aiState === 'chase' || aiState === 'search') return `${prefix}_walk`;
  return `${prefix}_idle`;
}
